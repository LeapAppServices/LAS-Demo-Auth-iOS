#import "UserDetailTableViewController.h"
#import "TrdPartyProfileViewController.h"
#import "AppDelegate.h"

@interface UserDetailTableViewController ()
@property (nonatomic, strong) NSArray *platforms;
@end

@implementation UserDetailTableViewController

- (void)awakeFromNib {
    [super awakeFromNib];
    
    MLSite region = [(AppDelegate *)[UIApplication sharedApplication].delegate region];
    if (region == MLSiteUS) {
        self.platforms = @[@"Facebook Profile"];
    } else if (region == MLSiteCN) {
        self.platforms = @[@"Weibo Profile", @"WeChat Profile"];
    }
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    // Uncomment the following line to preserve selection between presentations.
    // self.clearsSelectionOnViewWillAppear = NO;
    
    // Uncomment the following line to display an Edit button in the navigation bar for this view controller.
    // self.navigationItem.rightBarButtonItem = self.editButtonItem;
    
    // Add logout navigation bar button
    UIBarButtonItem *logoutButton = [[UIBarButtonItem alloc] initWithTitle:@"Log Out" style:UIBarButtonItemStyleBordered target:self action:@selector(logoutButtonTouchHandler:)];
    self.navigationItem.leftBarButtonItem = logoutButton;
}

- (void)logoutButtonTouchHandler:(id)sender {
    // Logout user, this automatically clears the cache
    [MLUser logOut];
    
    // Return to login view controller
    [self.navigationController popToRootViewControllerAnimated:YES];
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.platforms.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
    
    // Configure the cell...
    cell.textLabel.text = self.platforms[indexPath.row];
    
    return cell;
}

